defmodule Alpina.Application do
  # See https://hexdocs.pm/elixir/Application.html
  # for more information on OTP Applications
  @moduledoc false

  use Application

  @impl true
  def start(_type, _args) do
    children = [
      AlpinaWeb.Telemetry,
      Alpina.Repo,
      {DNSCluster, query: Application.get_env(:alpina, :dns_cluster_query) || :ignore},
      {Phoenix.PubSub, name: Alpina.PubSub},
      # Start a worker by calling: Alpina.Worker.start_link(arg)
      # {Alpina.Worker, arg},
      # Start to serve requests, typically the last entry
      AlpinaWeb.Endpoint
    ]

    # See https://hexdocs.pm/elixir/Supervisor.html
    # for other strategies and supported options
    opts = [strategy: :one_for_one, name: Alpina.Supervisor]
    Supervisor.start_link(children, opts)
  end

  # Tell Phoenix to update the endpoint configuration
  # whenever the application is updated.
  @impl true
  def config_change(changed, _new, removed) do
    AlpinaWeb.Endpoint.config_change(changed, removed)
    :ok
  end
end
